/***************************************************************************************************
 * @author Kevin Chamberlain
 * Gazelle class
 **************************************************************************************************/
public class Gazelle extends Animal{

    /**********************************************************************************************
     * Basic Constructor for the Gazelle
     *********************************************************************************************/
    public Gazelle(){
        health = 10;
        longevity = 2;
        name = "Gazelle";
        abrv = "G";
        color = "-fx-background-color:Tan;";
    }

    /**********************************************************************************************
     * Updates health after clicking "New Day"
     *********************************************************************************************/
    public void newDay() {
        health = health - longevity;

    }
}
